/** YYYY-MM-DD offset from today — keeps review-date demos always relevant. */
function d(offsetDays: number): string {
  const now = new Date();
  const t = new Date(now.getFullYear(), now.getMonth(), now.getDate() + offsetDays);
  const tz = t.getTimezoneOffset() * 60000;
  return new Date(t.getTime() - tz).toISOString().slice(0, 10);
}
function ts(offsetDays: number): string {
  return new Date(Date.now() + offsetDays * 86_400_000).toISOString();
}

function sys(s: Omit<AISystem, 'createdAt' | 'updatedAt'> & { createdDays: number }): AISystem {
  const { createdDays, ...rest } = s;
  return { ...rest, createdAt: ts(createdDays), updatedAt: ts(createdDays + 2) };
}
function risk(
  r: Omit<AIRisk, 'severity' | 'createdAt' | 'updatedAt'> & { createdDays: number }
): AIRisk {
  const { createdDays, ...rest } = r;
  return {
    ...rest,
    severity: computeSeverity(rest.likelihood, rest.impact),
    createdAt: ts(createdDays),
    updatedAt: ts(createdDays + 1),
  };
}
function ctrl(c: Omit<Control, 'createdAt' | 'updatedAt'> & { createdDays: number }): Control {
  const { createdDays, ...rest } = c;
  return { ...rest, createdAt: ts(createdDays), updatedAt: ts(createdDays + 1) };
}
function evd(e: Omit<Evidence, 'createdAt' | 'updatedAt'> & { createdDays: number }): Evidence {
  const { createdDays, ...rest } = e;
  return { ...rest, createdAt: ts(createdDays), updatedAt: ts(createdDays + 1) };
}
function dec(x: Omit<Decision, 'createdAt' | 'updatedAt'> & { createdDays: number }): Decision {
  const { createdDays, ...rest } = x;
  return { ...rest, createdAt: ts(createdDays), updatedAt: ts(createdDays + 1) };
}
function inc(x: Omit<Incident, 'createdAt' | 'updatedAt'> & { createdDays: number }): Incident {
  const { createdDays, ...rest } = x;
  return { ...rest, createdAt: ts(createdDays), updatedAt: ts(createdDays + 1) };
}
function gap(x: Omit<GapAction, 'createdAt' | 'updatedAt'> & { createdDays: number }): GapAction {
  const { createdDays, ...rest } = x;
  return { ...rest, createdAt: ts(createdDays), updatedAt: ts(createdDays + 1) };
}

